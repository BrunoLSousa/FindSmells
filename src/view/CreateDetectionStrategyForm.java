    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        if (!this.jTextAreaDetectionStrategy.getText().isEmpty() && !this.jTextFieldNameDetectionStrategy.getText().isEmpty()) {
            String expression = this.jTextAreaDetectionStrategy.getText();
            Evaluator evaluator = new Evaluator();
            if (evaluator.evaluate(expression)) {
                PutThresholdForm putThreshold = new PutThresholdForm(this.jTextFieldNameDetectionStrategy.getText(), this.granularity, expression, this.frame);
                putThreshold.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect expression! Check for errors in the expression.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Make sure that the fields \"Name of Detection Strategy\" and \"Expression\" are completed!", "Attention", JOptionPane.WARNING_MESSAGE, null);
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

