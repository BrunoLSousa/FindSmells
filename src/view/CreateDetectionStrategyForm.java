    private void jButtonBackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackspaceActionPerformed
        if (!jTextAreaDetectionStrategy.getText().isEmpty()) {
            String[] split = this.jTextAreaDetectionStrategy.getText().split(" ");
            String strategy = "";
            for (int i = 0; i < (split.length - 1); i++) {
                strategy += split[i] + " ";
            }
            this.jTextAreaDetectionStrategy.setText(strategy);
        }
    }//GEN-LAST:event_jButtonBackspaceActionPerformed

    private void initPanels() {
        enablePanelType(true);
        enablePanelMethods(false);
        enablePanelPackage(false);
        this.jRadioButtonType.setSelected(true);
        this.granularity = this.jRadioButtonType.getText();
    }

    private void initPanels(String granularity) {
        if (granularity.equals("Method")) {
            enablePanelMethods(true);
            enablePanelPackage(false);
            enablePanelType(false);
            this.jRadioButtonMethod.setSelected(true);
        } else if (granularity.equals("Package")) {
            enablePanelMethods(false);
            enablePanelPackage(true);
            enablePanelType(false);
            this.jRadioButtonPackage.setSelected(true);
        } else if (granularity.equals("Class")) {
            enablePanelMethods(false);
            enablePanelPackage(false);
            enablePanelType(true);
            this.jRadioButtonType.setSelected(true);
        }
        this.granularity = granularity;
    }

